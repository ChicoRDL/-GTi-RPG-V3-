local disabledHUD = {"health", "armour", "breath", "clock", "money", "weapon", "ammo", "vehicle_name", "area_name", "wanted"} 
addEvent("modshop.onClientStopModdingVehicle",true)
addEventHandler("modshop.onClientStopModdingVehicle",root,function()
	setTimer(function()
	if exports.GTIhud:isCustomHudEnabled() then
		for i,hud in ipairs(disabledHUD) do
			showPlayerHudComponent(hud, false)
		end
	end
	toggleControl("vehicle_fire", false)
	toggleControl("vehicle_secondary_fire", false)
	end,50,1)
	for i,v in pairs(pickerTable) do
		closePicker(i)
	end
end)
